package com.nfeanalytics.core.helper;

import java.math.BigDecimal;

public class FretePorEstadoHelper {

	private String		uf;

	private BigDecimal	valor;

	public FretePorEstadoHelper() {
	}

	public FretePorEstadoHelper(String uf, BigDecimal valor) {
		super();
		this.uf = uf;
		this.valor = valor;
	}

	public String getUf() {
		return uf;
	}

	public void setUf(String uf) {
		this.uf = uf;
	}

	public BigDecimal getValor() {
		return valor;
	}

	public void setValor(BigDecimal valor) {
		this.valor = valor;
	}

}
